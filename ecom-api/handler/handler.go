func toStorerOrder(order OrderReq) *storer.Order {
	return &storer.Order{
		PaymentMethod: order.PaymentMethod,
		TaxPrice:      order.TaxPrice,
		ShippingPrice: order.ShippingPrice,
		TotalPrice:    order.TotalPrice,
		Items:         toStorerOrderItems(order.Items),
	}
}

func toStorerOrderItems(items []*OrderItem) []storer.OrderItem {
	var res []storer.OrderItem
	for _, i := range items {
		res = append(res, storer.OrderItem{
			Name:      i.Name,
			Image:     i.Image,
			Quantity:  i.Quantity,
			Price:     float64(i.Price),
			ProductID: i.ProductID,
		})
	}

	return res
}

func toOrderRes(order *storer.Order) OrderRes {
	return OrderRes{
		ID:            order.ID,
		Items:         toOrderItems(order.Items),
		PaymentMethod: order.PaymentMethod,
		TaxPrice:      order.TaxPrice,
		ShippingPrice: order.ShippingPrice,
		TotalPrice:    order.TotalPrice,
		CreatedAt:     order.CreatedAt,
		UpdatedAt:     order.UpdatedAt,
	}
}

func toOrderItems(items []storer.OrderItem) []*OrderItem {
	var res []*OrderItem

	for _, i := range items {
		res = append(res, &OrderItem{
			Name:      i.Name,
			Quantity:  i.Quantity,
			Image:     i.Image,
			Price:     i.Price,
			ProductID: i.ProductID,
		})
	}

	return res
}
